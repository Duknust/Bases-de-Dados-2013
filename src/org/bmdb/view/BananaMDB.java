    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        Filme filme = (Filme) (jListTodosFilmesPesquisa.getSelectedValue());
        filme.setTitulo(jFormattedTextField10.getText());
        Realizador realizador = (Realizador) jComboBox1.getSelectedItem();
        filme.setRealizador(realizador);
        Produtora produtora = (Produtora) jComboBox2.getSelectedItem();
        filme.setProdutora(produtora);

        filme.setAno(Integer.parseInt(jFormattedTextField2.getText()));
        String ratingEscolhido = jComboBox3.getSelectedItem().toString();
        int rating = 0;
        if (ratingEscolhido.equals("all")) {
            rating = 0;
        } else if (ratingEscolhido.equals(">=6")) {
            rating = 6;
        } else if (ratingEscolhido.equals(">=8")) {
            rating = 8;
        } else if (ratingEscolhido.equals(">=12")) {
            rating = 12;
        } else if (ratingEscolhido.equals(">=16")) {
            rating = 16;
        } else if (ratingEscolhido.equals(">=18")) {
            rating = 18;
        } else if (ratingEscolhido.equals(">=21")) {
            rating = 21;
        }
        filme.setRating(rating);
        if ((int) jSpinner1.getValue() >= (int) jSpinner2.getValue() && (int) jSpinner2.getValue() >= (int) jSpinner3.getValue()) {
            filme.setPremiosNomeado((int) jSpinner1.getValue());
            filme.setPremiosVencidos((int) jSpinner2.getValue());
            filme.setOscares((int) jSpinner3.getValue());
        } else {
            JOptionPane.showMessageDialog(this, "Algo está mal nos prémios vencidos", "Error", JOptionPane.INFORMATION_MESSAGE, icon);
        }

        filme.setBudget(Integer.parseInt(jFormattedTextField11.getText()));
        filme.setGross(Integer.parseInt(jFormattedTextField12.getText()));
        filme.setSinopse(jTextField1.getText());
        filme.setTrailerUri(jFormattedTextField16.getText());
        filme.setPosterUri(jFormattedTextField17.getText());

        List<Genero> generosNovos = new ArrayList<>();
        List<Personagem> personagensNovas = new ArrayList<>();

        for (int i = 0; i < jList1.getModel().getSize(); i++) {
            personagensNovas.add((Personagem) jList1.getModel().getElementAt(i));
        }
        for (int i = 0; i < jList2.getModel().getSize(); i++) {
            generosNovos.add((Genero) jList2.getModel().getElementAt(i));
        }

        filme.setPersonagens(personagensNovas);
        filme.setGeneros(generosNovos);

        if (mp != null && mr != null && mf != null) {
            try {
                mf.update(filme);
            } catch (BslInsertionFailedException ex) {
                Logger.getLogger(BananaMDB.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton10ActionPerformed

