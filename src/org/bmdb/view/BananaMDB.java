    private void jListTodosFilmesPesquisaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListTodosFilmesPesquisaValueChanged
        Filme filme = (Filme) jListTodosFilmesPesquisa.getSelectedValue();


        if (filme != null) {
            jFormattedTextField10.setText(filme.getTitulo());
            jComboBox1.setSelectedItem(filme.getRealizador());
            jComboBox2.setSelectedItem(filme.getProdutora());
            jFormattedTextField2.setText(filme.getAno() + "");
            if (filme.getRating() < 6) {
                jComboBox3.setSelectedIndex(0);
            } else if (filme.getRating() < 8) {
                jComboBox3.setSelectedIndex(1);
            } else if (filme.getRating() < 12) {
                jComboBox3.setSelectedIndex(2);
            } else if (filme.getRating() < 16) {
                jComboBox3.setSelectedIndex(3);
            } else if (filme.getRating() < 18) {
                jComboBox3.setSelectedIndex(4);
            } else if (filme.getRating() < 21) {
                jComboBox3.setSelectedIndex(5);
            } else {
                jComboBox3.setSelectedIndex(6);
            }

            jSpinner1.setValue(filme.getPremiosNomeado());
            jSpinner2.setValue(filme.getPremiosVencidos());
            jSpinner3.setValue(filme.getOscares());
            jFormattedTextField11.setText(filme.getBudget() + "");
            jFormattedTextField12.setText(filme.getGross() + "");
            jTextField1.setText(filme.getSinopse());
            jFormattedTextField16.setText(filme.getTrailerUri());
            jFormattedTextField17.setText(filme.getPosterUri());
            //fim de filme, inicio de actores do filme

            List<Actor> actores = ma.getAll();
            for (Actor act : actores) {
                jComboBox4.addItem(act);
            }
            DefaultListModel listaActor = new DefaultListModel();
            List<Personagem> personagens = filme.getPersonagens();
            if (personagens != null && !personagens.isEmpty()) {


                for (Personagem personagem : personagens) {
                    listaActor.addElement(personagem);
                }
            }
            jList1.setModel(listaActor);
            //inicio dos generos filme

            List<Genero> totalGeneros = mg.getAll();
            for (Genero gen : totalGeneros) {
                jComboBox5.addItem(gen);
            }

            DefaultListModel listaGenero = new DefaultListModel();
            List<Genero> generos = filme.getGeneros();
            if (generos != null && !generos.isEmpty()) {
                for (Genero genero : generos) {
                    listaGenero.addElement(genero);
                }
            }
            jList2.setModel(listaGenero);

            jLabel1.setText("");
            jLabel1.setIcon(new ImageIcon("images/posters/" + filme.getPosterUri()));
        }

    }//GEN-LAST:event_jListTodosFilmesPesquisaValueChanged

    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        //tipo de ligaçao
        Properties props = null;
        boolean redMessage = true;

        switch (jComboBox15.getSelectedIndex()) {
            case 0: // Oracle
                props = new Properties();
                props.setProperty("oracle.ip", jFormattedTextField5.getText().trim());
                props.setProperty("oracle.port", jFormattedTextField6.getText().trim());
                props.setProperty("oracle.dbname", jFormattedTextField1.getText().trim());
                props.setProperty("oracle.user", jFormattedTextField8.getText().trim());
                props.setProperty("oracle.password", new String(jPasswordField1.getPassword()).trim());
                props.setProperty("connectionbroker.impl", jComboBox15.getSelectedItem().toString().trim());
                break;
            case 1:
                props = new Properties();
                props.setProperty("sqlite.datafile", jFormattedTextField9.getText());
                props.setProperty("connectionbroker.impl", jComboBox15.getSelectedItem().toString());
                break;
            default:
                break;
        }
        try {
            this.initManagers(props);
            jLabel78.setText("Connection Successful!");
            jLabel78.setForeground(Color.green);
            redMessage = false;
            this.initBoxes();
            this.props = props;

        } catch (ViewDataManagerException ex) {
            ex.printStackTrace(System.err);
            Logger
                    .getLogger(BananaMDB.class
                    .getName()).log(Level.SEVERE, null, ex.getStackTrace());
        } finally {
            if (redMessage) {
                jLabel78.setText("Connection Error!");
                jLabel78.setForeground(Color.red);
            }
        }
    }//GEN-LAST:event_jButton19ActionPerformed
