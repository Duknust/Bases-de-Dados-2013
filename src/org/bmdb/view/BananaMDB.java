    private void initBoxes() {
        List<Genero> generos = mg.getAll();
        List<Produtora> produtoras = mp.getAll();
        List<Realizador> realizadores = mr.getAll();
        List<Actor> actores = ma.getAll();
        List<Filme> filmes = mf.getAll();
        for (Genero g : generos) {
            jComboBox5.addItem(g);
        }
        for (Produtora p : produtoras) {
            jComboBox2.addItem(p);
        }
        for (Realizador r : realizadores) {
            jComboBox1.addItem(r);
        }
        for (Actor a : actores) {
            jComboBox4.addItem(a);
        }
        for (Filme f : filmes) {
            jComboBox21.addItem(f);
        }

    }
