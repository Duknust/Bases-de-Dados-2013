    private void jList6ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList6ValueChanged
        Utilizador utilizador = (Utilizador) jList6.getSelectedValue();
        int i;
        jFormattedTextField3.setText(utilizador.getUsername());
        jFormattedTextField4.setText(utilizador.getEmail());

        char[] dataNascimento = utilizador.getDataNascimento().toCharArray();
        StringBuilder dia = new StringBuilder();
        StringBuilder mes = new StringBuilder();
        StringBuilder ano = new StringBuilder();
        for (i = 0; dataNascimento[i] != '-'; i++) {
            dia.append(dataNascimento[i]);
        }
        for (i = i + 1; dataNascimento[i] != '-'; i++) {
            mes.append(dataNascimento[i]);
        }
        for (i = i + 1; i < dataNascimento.length; i++) {
            ano.append(dataNascimento[i]);
        }
        jComboBox6.setSelectedItem(dia.toString());
        jComboBox7.setSelectedItem(mes.toString());
        jComboBox8.setSelectedItem(ano.toString());

        jFormattedTextField7.setText(utilizador.getAvatar());
        jComboBox16.setSelectedIndex(utilizador.getEstado() - 1);
        jLabel92.setText("");
        jLabel92.setIcon(new ImageIcon("images/users/" + utilizador.getAvatar()));
    }//GEN-LAST:event_jList6ValueChanged

