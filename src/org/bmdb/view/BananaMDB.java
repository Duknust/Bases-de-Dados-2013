    private void jList7ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList7ValueChanged
        Actor actor = (Actor) jList7.getSelectedValue();
        jFormattedTextFieldNomeActor.setText(actor.getNomeActor());
        int i;
        char[] dataNascimento = actor.getDataNascimentoActor().toCharArray();
        StringBuilder diaNasc = new StringBuilder();
        StringBuilder mesNasc = new StringBuilder();
        StringBuilder anoNasc = new StringBuilder();
        for (i = 0; dataNascimento[i] != '-'; i++) {
            diaNasc.append(dataNascimento[i]);
        }
        for (i = i + 1; dataNascimento[i] != '-'; i++) {
            mesNasc.append(dataNascimento[i]);
        }
        for (i = i + 1; i < dataNascimento.length; i++) {
            anoNasc.append(dataNascimento[i]);
        }
        jComboBoxDNActorDia.setSelectedItem(diaNasc.toString());
        jComboBoxDNActorMes.setSelectedItem(mesNasc.toString());
        jComboBoxDNActorAno.setSelectedItem(anoNasc.toString());
        if (actor.getDataMorteActor() != null && !actor.getDataMorteActor().isEmpty()) {
            jComboBoxDMActorDia.setSelectedIndex(-1);
            jComboBoxDMActorMes.setSelectedIndex(-1);
            jComboBoxDMActorAno.setSelectedIndex(-1);

            jComboBoxDMActorDia.setEnabled(true);
            jComboBoxDMActorMes.setEnabled(true);
            jComboBoxDMActorAno.setEnabled(true);
            jCheckBox1.setSelected(false);
            char[] dataMorte = actor.getDataMorteActor().toCharArray();
            StringBuilder diaMorte = new StringBuilder();
            StringBuilder mesMorte = new StringBuilder();
            StringBuilder anoMorte = new StringBuilder();
            for (i = 0; dataMorte[i] != '-'; i++) {
                diaMorte.append(dataMorte[i]);
            }
            for (i = i + 1; dataMorte[i] != '-'; i++) {
                mesMorte.append(dataMorte[i]);
            }
            for (i = i + 1; i < dataMorte.length; i++) {
                anoMorte.append(dataMorte[i]);
            }
            jComboBoxDMActorDia.setSelectedItem(diaMorte.toString());
            jComboBoxDMActorMes.setSelectedItem(mesMorte.toString());
            jComboBoxDMActorAno.setSelectedItem(anoMorte.toString());
        } else {
            jCheckBox1.setSelected(true);
            jComboBoxDMActorDia.setEnabled(false);
            jComboBoxDMActorMes.setEnabled(false);
            jComboBoxDMActorAno.setEnabled(false);
        }
        jSpinner4.setValue(actor.getPremiosNomeadoActor());
        jSpinner5.setValue(actor.getPremiosVencidosActor());
        jSpinner6.setValue(actor.getOscaresVencidos());
        jComboBox12.setSelectedItem(actor.getNacionalidadeActor().toString());
        if (actor.getSexo().equals("Masculino")) {
            jRadioButton1.setSelected(true);
        } else if (actor.getSexo().equals("Feminino")) {
            jRadioButton2.setSelected(true);
        }
        jFormattedTextFieldImagemActor.setText(actor.getImagem());
        jLabel90.setText("");
        jLabel90.setIcon(new ImageIcon("images/actores/" + actor.getImagem()));
    }//GEN-LAST:event_jList7ValueChanged

