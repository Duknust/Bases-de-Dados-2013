    private void jList8ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList8ValueChanged
        // TODO add your handling code here:
        Noticia noticia = (Noticia) jList8.getSelectedValue();
        jFormattedTextField13.setText(noticia.getTitulo());
        jFormattedTextField14.setText(noticia.getFonte());
        String dataorig = noticia.getData();
        StringBuilder diaS = new StringBuilder();
        StringBuilder mesS = new StringBuilder();
        StringBuilder anoS = new StringBuilder();
        int i;
        for (i = 0; dataorig.charAt(i) != '-'; i++) {
            diaS.append(dataorig.charAt(i));
        }
        for (i = i + 1; dataorig.charAt(i) != '-'; i++) {
            mesS.append(dataorig.charAt(i));
        }
        for (i = i + 1; i < dataorig.length(); i++) {
            anoS.append(dataorig.charAt(i));
        }
        jComboBox18.setSelectedItem(diaS.toString());
        jComboBox19.setSelectedItem(mesS.toString());
        jComboBox20.setSelectedItem(anoS.toString());
        jFormattedTextField15.setText(noticia.getSinopse());
        jFormattedTextField18.setText(noticia.getCorpo());
        jFormattedTextField19.setText(noticia.getImagem());
        jFormattedTextField20.setText(noticia.getVideo());
        if (noticia.getFilme() == null) {
            Filme filme = this.mf.procuraPorId(noticia.getIdFilme());
            noticia.setFilme(filme);
        }
        jComboBox21.setSelectedItem(noticia.getFilme());
        jLabel91.setIcon(new ImageIcon("images/noticias/" + noticia.getImagem()));
    }//GEN-LAST:event_jList8ValueChanged

